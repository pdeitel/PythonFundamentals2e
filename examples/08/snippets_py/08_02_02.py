## 8.2.2 Field Widths and Alignment

f'[{27:10d}]'

f'[{3.5:10f}]'

f'[{"hello":10}]'

### Explicitly Specifying Left and Right Alignment in a Field

f'[{27:<10d}]'

f'[{3.5:<10f}]'

f'[{"hello":>10}]'

### Centering a Value in a Field

f'[{27:^10d}]'

f'[{3.5:^10.1f}]'

f'[{"hello":^10}]'

