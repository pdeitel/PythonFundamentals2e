## 8.2.4 String Method format

'{:.2f}'.format(17.489)

### Multiple Placeholders

'{} {}'.format('Tiare', 'Maihi')

### Referencing Arguments By Position Number

'{0} {0} {1}'.format('Happy', 'Birthday')

### Referencing Keyword Arguments

'{first} {last}'.format(first='Leon', last='Weber')

'{last}, {first}'.format(first='Leon', last='Weber')

