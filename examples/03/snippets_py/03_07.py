## 3.7 match…case Selection Statement

grade = 'C'

match grade:
    case 'A':
        print('grade is A')
    case 'B':
        print('grade is B')
    case 'C':
        print('grade is C')
    case 'D':
        print('grade is D')
    case _:  # "default" case executes if no other cases match
        print('grade is F')

